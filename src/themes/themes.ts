/** Map old theme keys to new ones for backward compatibility */
export const legacyThemeMap: Record<string, string> = {
  minimal: 'modern-minimal',
  noir: 'noir-classic',
  editorial: 'warm-luxury',
  spotlight: 'cinematic-dark',
  ink: 'cinematic-dark',
  brutalist: 'modern-minimal',
  modernist: 'modern-minimal',
  warm: 'mediterranean',
  midnight: 'midnight-glass',
  gallery: 'frost',
};

export function resolveThemeId(themeKey: string | null | undefined): string {
  if (!themeKey) return 'cinematic-dark';
  if (portfolioThemes[themeKey]) return themeKey;
  return legacyThemeMap[themeKey] || 'cinematic-dark';
}

export function getPortfolioTheme(themeKey: string | null | undefined): PortfolioTheme {
  return portfolioThemes[resolveThemeId(themeKey)];
}

/** Get all Google Font families needed for a theme */
export function getThemeFontFamilies(theme: PortfolioTheme): string[] {
  const families = new Set<string>();
  const fonts = [theme.fontDisplay, theme.fontBody, theme.fontLogline];
  for (const f of fonts) {
    const match = f.match(/'([^']+)'/);
    if (match) {
      const name = match[1];
      if (!['Georgia', 'Garamond', 'Impact', 'Helvetica Neue', 'Courier New'].includes(name)) {
        families.add(name);
      }
    }
  }
  return Array.from(families);
}

/** Build Google Fonts URL for given families */
export function buildGoogleFontsUrl(families: string[]): string {
  if (families.length === 0) return '';
  const params = families.map(f =>
    `family=${f.replace(/ /g, '+')}:ital,wght@0,300;0,400;0,500;0,600;0,700;0,800;1,400;1,600`
  ).join('&');
  return `https://fonts.googleapis.com/css2?${params}&display=swap`;
}

/** Get URL for ALL theme fonts (used on demo/settings pages) */
export function getAllThemeFontsUrl(): string {
  const allFamilies = new Set<string>();
  for (const theme of portfolioThemeList) {
    for (const f of getThemeFontFamilies(theme)) {
      allFamilies.add(f);
    }
  }
  return buildGoogleFontsUrl(Array.from(allFamilies));
}
